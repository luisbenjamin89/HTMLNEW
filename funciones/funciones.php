<?php
function sumar(int $numero1 , int $numero2):int
{
    $resultado= $numero1 + $numero2;
    return $resultado;
}

function restar(int $numero1 , int $numero2):int
{
    $resultado= $numero1 - $numero2;
    return $resultado;
}

function multiplicar(int $numero1 , int $numero2):int
{
    $resultado= $numero1 * $numero2;
    return $resultado;
}

function Dividir(int $numero1 , int $numero2):float
{
    $resultado= $numero1 / $numero2;
    return $resultado;
}